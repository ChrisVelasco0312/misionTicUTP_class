def imprimir(texto):
    print(texto)


def sumar_numeros(numero_1, numero_2):
    resultado = numero_1 + numero_2
    print(resultado)


def restar_numeros(numero_1, numero_2):
    resultado = numero_1 - numero_2
    print(resultado)


def multiplicar_numeros(numero_1, numero_2):
    resultado = numero_1 * numero_2
    print(resultado)


def dividir_numeros(numero_1, numero_2):
    resultado = numero_1 / numero_2
    print(resultado)
